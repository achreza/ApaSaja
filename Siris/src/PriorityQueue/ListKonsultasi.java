    private void tabelKonsulMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelKonsulMouseClicked
        try {
            // TODO add your handling code here:
            int row = tabelKonsul.getSelectedRow();
            String nik, pasien, dokter, spesialis, keluhan, tanggal;
            nik = tabelKonsul.getModel().getValueAt(row, 0).toString();
            pasien = tabelKonsul.getModel().getValueAt(row, 1).toString();
            dokter = tabelKonsul.getModel().getValueAt(row, 2).toString();
            spesialis = tabelKonsul.getModel().getValueAt(row, 3).toString();
            keluhan = tabelKonsul.getModel().getValueAt(row, 4).toString();
            tanggal = tabelKonsul.getModel().getValueAt(row, 5).toString();
            
            String query = "select * from dokter where nama = '" + dokter+"'";
            ResultSet rs = stm.executeQuery(query);
            while (rs.next()) {
                
                lblNamaDokter.setText(rs.getString("nama"));
                txtSpesialis.setText(rs.getString("spesialis"));
                String file1;
                file1 = rs.getString("foto");
                
                ImageIcon icon = new ImageIcon(file1);
                fotoPlaceholder.setIcon(icon);
            }
            
            String query2 = "select * from data_pasien where nik = " + nik+"";
            ResultSet rs2 = stm.executeQuery(query2);
            while (rs2.next()) {
                txtNik.setText(rs2.getString("nik"));
                txtNama.setText(rs2.getString("nama"));
                txtUmur.setText(rs2.getString("umur"));
                cbJk.setSelectedItem(rs2.getString("jk"));
                txtTelpon.setText(rs2.getString("telpon"));
                txtAlamat.setText(rs2.getString("alamat"));
                txtKeluhan.setText(keluhan);
                lblTanggal.setText("Tanggal : "+tanggal);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(ListKonsultasi.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
        
    }//GEN-LAST:event_tabelKonsulMouseClicked

