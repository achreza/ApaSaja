    public void selectData() throws SQLException {
        String query = "SELECT konsultasi.nik,data_pasien.nama,dokter.nama,dokter.spesialis,konsultasi.keluhan,konsultasi.tanggal from konsultasi join data_pasien ON konsultasi.nik = data_pasien.nik JOIN dokter ON konsultasi.id_dokter = dokter.id;";
        ResultSet rs = stm.executeQuery(query);
        while (rs.next()) {
            this.insertTable(rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getString(5), rs.getString(6));
        }
    }
    
